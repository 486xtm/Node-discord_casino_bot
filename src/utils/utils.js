function getCardEmoji(card) {
  return cardEmojis[card] || card;
}
function formatHand(hand, hideSecond = false) {
  if (hideSecond) {
    return `${getCardEmoji(hand[0])} ${getCardEmoji('BACK')}`;
  }
  return hand.map(card => getCardEmoji(card)).join(' ');
}

